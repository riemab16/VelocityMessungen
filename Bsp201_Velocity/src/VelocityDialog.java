    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        try {
            LocalDateTime datum = LocalDateTime.parse(tfDate.getText() + " " + tfTime.getText(), DateTimeFormatter.ofPattern("dd.MM.yyyy HH:mm"));
            String kz = tfKennzeichen.getText();
            int vErlaubt = Integer.parseInt(tfErlaubt.getText());
            double vGemessen = Double.parseDouble(tfGemessen.getText());
            this.dispose();
            m = new Measurement(datum, kz, vErlaubt, vGemessen);
            isOK = true;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Falsche Eingabe.");
        }
        
    }//GEN-LAST:event_btAddActionPerformed

